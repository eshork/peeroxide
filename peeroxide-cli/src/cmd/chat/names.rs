pub fn generate_name_from_seed(seed: &[u8; 32]) -> String {
    let mut rng = StdRng::from_seed(*seed);
    let adj = ADJECTIVES[rng.random_range(0..ADJECTIVES.len())];
    let surname = SURNAMES[rng.random_range(0..SURNAMES.len())];
    let num: u16 = rng.random_range(0..10000);
    format!("{adj}_{surname}_{num:04}")
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn generate_name_is_deterministic() {
        let seed = [42u8; 32];
        let name1 = generate_name_from_seed(&seed);
        let name2 = generate_name_from_seed(&seed);
        assert_eq!(name1, name2);
    }

    #[test]
    fn generate_name_known_seed() {
        let seed = [0u8; 32];
        let name = generate_name_from_seed(&seed);
        let parts: Vec<&str> = name.splitn(3, '_').collect();
        assert_eq!(parts.len(), 3, "name must have exactly two underscores: {name}");
        assert_eq!(parts[2].len(), 4, "suffix must be 4 digits: {name}");
        assert!(parts[2].chars().all(|c| c.is_ascii_digit()), "suffix must be digits: {name}");
    }

    #[test]
    fn generate_name_format_regex_like() {
        let seed = [1u8; 32];
        let name = generate_name_from_seed(&seed);
        assert!(
            name.chars().all(|c| c.is_ascii_lowercase() || c.is_ascii_digit() || c == '_'),
            "unexpected chars in: {name}"
        );
        assert_eq!(name.chars().filter(|&c| c == '_').count(), 2, "expected 2 underscores in: {name}");
    }

    #[test]
    fn generate_name_different_seeds_differ() {
        let seed_a = [0u8; 32];
        let seed_b = [1u8; 32];
        let name_a = generate_name_from_seed(&seed_a);
        let name_b = generate_name_from_seed(&seed_b);
        assert_ne!(name_a, name_b, "different seeds should (very likely) produce different names");
    }
}
